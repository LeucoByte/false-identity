import os
import sys
import signal

from config import BOLD, GREEN, YELLOW, CYAN, RESET


def signal_handler(sig, frame):
    """
    Handle Ctrl+C gracefully.

    Shows a goodbye message and clears screen before exit.
    """
    print(f"\n\n{BOLD}{GREEN}Goodbye!{RESET}\n")
    clear_screen()
    sys.exit(0)


def clear_screen():
    """Clear the terminal screen (cross-platform)."""
    os.system('clear' if os.name != 'nt' else 'cls')


